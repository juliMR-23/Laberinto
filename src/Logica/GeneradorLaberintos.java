    private static char[][] rotar(char[][] matriz) {
        int filas = matriz.length;
        int columnas = matriz[0].length;
        char[][] invertida = new char[columnas][filas];
        for (int i = 0; i < filas; i++) {
            for (int j = 0; j < columnas; j++) {
                invertida[j][i] = matriz[i][j];
            }
        }
        return invertida;
    }
    
    private static char[][] crearVacio(int alto, int ancho) {
        char[][] nuevoLab = new char[alto][ancho];
        
        for (int i = 0; i < alto; i++) {
            for (int j = 0; j < ancho; j++) {//Pone muro solo en los bordes
                if (i == 0 || i == alto - 1 || j == 0 || j == ancho - 1) {
                    nuevoLab[i][j] = '#';
                } else {
                    nuevoLab[i][j] = ' '; //celdas libres
                }
            }
        }
        return nuevoLab;
    }
